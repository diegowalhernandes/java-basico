package edu;

public class Incremento {
    public static void main(String[] args){
        int numero = 5;

        System.out.println(numero);

        System.out.println(numero ++); //primeiro mostra o valor depois incrementa

        System.out.println(++ numero); // incrementa e traz o novo valor

        System.out.println(-- numero); // realiza o decremento

        boolean variavel = true;

        System.out.println(!variavel); //! inverte o valor da variavel

    }
    
}
